describe('day 10', () => {
  function getParsed(input) {
    const lines = input.split('\n');
    return { lines };
  }

  const braces = {
    '{': '}',
    '[': ']',
    '(': ')',
    '<': '>',
  };
  const errorScores = {
    ')': 3,
    ']': 57,
    '}': 1197,
    '>': 25137,
  };
  const stackScores = {
    ')': 1,
    ']': 2,
    '}': 3,
    '>': 4,
  };

  function getLineStatus(line) {
    // console.log('line', line);
    const starters = Object.keys(braces);
    const enders = Object.values(braces);
    const stack = [];
    const errors = [];
    let last = null;
    for (let i in line) {
      const ch = line[i];
      if (starters.includes(ch)) {
        stack.push(last);
        last = braces[ch];
      } else if (ch === last) {
        last = stack.pop();
      } else if (enders.includes(ch)) {
        errors.push({ ch, last, i });
      }
    }
    return { line, stack: [...stack.slice(1), last], errors };
  }

  test('part 1 example', () => {
    const { lines } = getParsed(exampleInput);

    const statusses = lines.map((line) => getLineStatus(line));
    const result = statusses
      .filter(({ errors }) => errors.length)
      .map(({ errors }) => errorScores[errors[0].ch])
      .reduce((acc, v) => acc + v);

    expect(result).toEqual(26397);
  });
  test('part 1', () => {
    const { lines } = getParsed(input);

    const statusses = lines.map((line) => getLineStatus(line));
    const result = statusses
      .filter(({ errors }) => errors.length)
      .map(({ errors }) => errorScores[errors[0].ch])
      .reduce((acc, v) => acc + v);

    expect(result).toEqual(168417);
  });

  test('part 2 example', () => {
    const { lines } = getParsed(exampleInput);

    const statusses = lines.map((line) => getLineStatus(line));
    const result = statusses
      .filter(({ errors }) => errors.length === 0)
      .map(({ stack }) =>
        stack.reverse().reduce((acc, ch) => acc * 5 + stackScores[ch], 0)
      )
      .sort((a, b) => (a > b ? 1 : a === b ? 0 : -1));
    console.log(result);

    expect(result[(result.length / 2) | 0]).toEqual(288957);
  });
  test('part 2', () => {
    const { lines } = getParsed(input);

    const statusses = lines.map((line) => getLineStatus(line));
    const result = statusses
      .filter(({ errors }) => errors.length === 0)
      .map(({ stack }) =>
        stack.reverse().reduce((acc, ch) => acc * 5 + stackScores[ch], 0)
      )
      .sort((a, b) => (a > b ? 1 : a === b ? 0 : -1));
    console.log(result);

    expect(result[(result.length / 2) | 0]).toEqual(2802519786);
  });
});

